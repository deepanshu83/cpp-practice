#include<iostream>
using namespace std;

int main(){

    int x = 10;
    cout<< x <<endl;
    cout<<endl;

    // post increment x++
    // x++ phle print karega or fir next time increase kar de ga 
    cout<< x++ <<endl;
    cout<< x <<endl;
    cout<< x <<endl;
    cout<<endl;

    // pre increment ++x
    // ++x phle increase kar de ga fir print karega
    cout<< ++x <<endl;
    cout<< x <<endl;
    cout<< x <<endl;
    cout<<endl;

    // post decrement x--
    // x-- phle print karega or fir next time decrease kar de ga 
    cout<< x-- <<endl;
    cout<< x <<endl;
    cout<< x <<endl;
    cout<<endl;

    // pre decrement --x
    // --x phle decrease kar de ga fir print karega
    cout<< --x <<endl; 
    cout<< x <<endl;
    cout<< x <<endl;
    cout<<endl;

    cout<< x <<endl;
    return 0;
}